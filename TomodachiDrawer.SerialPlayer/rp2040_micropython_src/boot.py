import usb_hid
import supervisor

# Spoofing the Hori ID (0x0F0D:0x0092)
# The Switch will show the Pro Controller icon for this.
supervisor.set_usb_identification(vid=0x0F0D, pid=0x0092)

# Professional Switch Analog Descriptor (8-byte report)
# Buttons (16 bits) | D-Pad (4 bits + 4 padding) | LX | LY | RX | RY
ANALOG_DESCRIPTOR = bytes((
    0x05, 0x01, 0x09, 0x05, 0xA1, 0x01, 0x15, 0x00, 
    0x25, 0x01, 0x35, 0x00, 0x45, 0x01, 0x75, 0x01, 
    0x95, 0x10, 0x05, 0x09, 0x19, 0x01, 0x29, 0x10, 
    0x81, 0x02, 0x05, 0x01, 0x25, 0x07, 0x46, 0x3b, 
    0x01, 0x75, 0x04, 0x95, 0x01, 0x65, 0x14, 0x09, 
    0x39, 0x81, 0x42, 0x65, 0x00, 0x95, 0x01, 0x81, 
    0x01, 0x26, 0xff, 0x00, 0x46, 0xff, 0x00, 0x09, 
    0x30, 0x09, 0x31, 0x09, 0x32, 0x09, 0x35, 0x75, 
    0x08, 0x95, 0x04, 0x81, 0x02, 0xc0
))

gamepad = usb_hid.Device(
    report_descriptor=ANALOG_DESCRIPTOR,
    usage_page=0x01,
    usage=0x05,
    report_ids=(0,),
    in_report_lengths=(8,),
    out_report_lengths=(0,),
)

usb_hid.enable((gamepad,))